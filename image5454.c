// 使用示例     search_reference_col(mt9v03x[0]);
// 备注信息     获取参考点
//-------------------------------------------------------------------------------------------------------------------
void search_reference_col(const uint8 *image)
{
    int col, row;
    uint8 temp1 = 0, temp2 = 0, temp3 = 0;

    for (col = 0; col < SEARCH_IMAGE_W; col++)
    {
        remote_distence[col] = SEARCH_IMAGE_H - 1;
    }

    for (col = 0; col < SEARCH_IMAGE_W; col += CONTRASTOFFSET)
    {
        for (row = SEARCH_IMAGE_H - 1; row > STOPROW; row -= CONTRASTOFFSET)
        {
            temp1 = *(image + row * SEARCH_IMAGE_W + col); // 获取当前点灰度值

            temp2 = *(image + (row - CONTRASTOFFSET) * SEARCH_IMAGE_W + col); // 获取对比点灰度值(获取方式存疑)

            if (temp2 > white_max_point)
            {
                continue;
            }
            if (temp1 < white_min_point)
            {
                remote_distence[col] = (uint8)row;
                break;
            }

            temp3 = (temp1 - temp2) * 200 / (temp1 + temp2);

            if (temp3 > reference_contrast_ratio || row == STOPROW)
            {
                remote_distence[col] = (uint8)row;
                break;
            }
        }
    }
    //    reference_col = find_extreme_value(remote_distence,10,SEARCH_IMAGE_W - 10,0) + CONTRASTOFFSET; //找最远白点列
    uint8 count = SEARCH_IMAGE_H;
    for (int i = 10; i < SEARCH_IMAGE_W - 10; i++)
    {
        if (count > remote_distence[i])
        {
            count = remote_distence[i];
            reference_col = i;
        }
    }
    reference_col = (uint8)func_limit_ab(reference_col, 1, SEARCH_IMAGE_W - 2); // 限幅

    for (int i = 0; i < SEARCH_IMAGE_H; i++)
    {
        reference_col_line[i] = reference_col; // 绘制参考列
    }
}
